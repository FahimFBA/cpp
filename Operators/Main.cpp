#include <bits/stdc++.h>
using namespace std;

int main()
{
    int a = 20;
    int b = 15;

    // Addition
    int sum = a + b;
    cout << "The summation is: " << sum << endl;

    // Subtraction
    int sub = a - b;
    cout << "The subtraction is: " << sub << endl;

    // Multiplication
    int multi = a * b;
    cout << "The multiplication is: " << multi << endl;

    // Division
    int div = a / b;
    cout << "The division is: " << div << endl;

    // Modulus
    int modulo = a % b;
    cout << "The modulus is: " << modulo << endl;

}